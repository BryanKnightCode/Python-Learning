name = "ada lovelace"
print(name.title())
# title() is a method. A Method is an action that Python can perform on a piece of data.
# The dot (.) after name (variable) in name.title() tells Python to make the title() method act on the variable name.


# The title() function doesn’t need any additional information, so its parentheses are empty.title() displays each word in titlecase, 
# where each word begins with a capital letter. 
# This is useful because you’ll often want to think of a name as a
# piece of information. For example, you might want your program to recognize the input values Ada, ADA, and ada as the same name, and display all of them as Ada.

